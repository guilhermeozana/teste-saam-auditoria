    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(campoEmail.getText().isEmpty() || campoSenha.getPassword().length == 0) {
            Util.getAlert().alertNormal("Informe o e-mail e a senha!");
           
            return;
        }
        
        if(loginController.fazerLogin(campoEmail.getText(), new String(campoSenha.getPassword()))) {

            if (this.funcionarioController == null) {
                this.funcionarioController = new FuncionarioController();
            } else {
                this.funcionarioController.getFuncionarioView().setVisible(true);
            }
            
            this.setVisible(false);
            
        } else {
            Util.getAlert().alertErro("Login ou Senha inv√°lidos!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

